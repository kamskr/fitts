abstract class AppSpacing {
  /// The default spacing unit
  static const double spaceUnit = 16;

  /// xxxs spacing value (2pt)
  static const double xxxs = 0.125 * spaceUnit;

  /// xxs spacing value (4pt)
  static const double xxs = 0.25 * spaceUnit;

  /// xs spacing value (8pt)
  static const double xs = 0.5 * spaceUnit;

  /// sm spacing value (12pt)
  static const double sm = 0.75 * spaceUnit;

  /// md spacing value (16pt)
  static const double md = spaceUnit;

  /// lg spacing value (24pt)
  static const double lg = 1.5 * spaceUnit;

  /// xlg spacing value (32pt)
  static const double xlg = 2 * spaceUnit;

  /// xxlg spacing value (40pt)
  static const double xxlg = 2.5 * spaceUnit;

  /// xxxlg pacing value (56pt)
  static const double xxxlg = 3.5 * spaceUnit;
}
